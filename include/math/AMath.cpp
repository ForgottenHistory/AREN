#include "AMath.h"

float ASin(float x)
{
    return std::sin(x);
}

float ACos(float x)
{
    return std::cos(x);
}

float ATan(float x)
{
    return std::atan(x);
}

float ATan2(float y, float x)
{
    return std::atan2(y, x);
}

float ASqrt(float x)
{
    return std::sqrt(x);
}

float APow(float x, float y)
{
    return std::pow(x, y);
}
